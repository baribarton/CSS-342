//------------------------------------------------------------------------------
#include "Customer.h"
//------------------------------------------------------------------------------

//------------------------------------------------------------------------------
// Constructor that sets the first and last names to empty strings and the id
// number to 0
//------------------------------------------------------------------------------
Customer::Customer(){
    firstName = "";
    lastName = "";
    id = 0;
}
//------------------------------------------------------------------------------

//------------------------------------------------------------------------------
// Sets the id, first name, and last name to what is being passed through in the
// parameter.
//------------------------------------------------------------------------------
Customer::Customer(int id, string fName, string lName){
    this->id = id;
    fName = firstName;
    lName = lastName;
}
//------------------------------------------------------------------------------

//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
Customer::~Customer(){
}
//------------------------------------------------------------------------------

//------------------------------------------------------------------------------
// Sets the names based on what is passed through the parameters.
//------------------------------------------------------------------------------
void Customer::setName(string fName, string lName){
    firstName = fName;
    lastName = lName;
}
//------------------------------------------------------------------------------

//------------------------------------------------------------------------------
// Gets the name and concatinates them together.
//------------------------------------------------------------------------------
string Customer::getName(){
    return firstName + lastName;
}
